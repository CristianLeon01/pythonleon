
#Se usan varios metodos del bucle for ##

#for con un condicional if
for i in range(11):
    if i%3==0:
        print(i, "es multiplo de 3")
    else:
        print(i)

#for para empezar como uno desee
for j in range (1,11):
    print(j)

#for para empezar donde uno quiere, donde finaliza y como se cuenta.
for k in range (0,11,2):
    print(k)
