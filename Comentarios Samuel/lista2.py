# Se crea una lista vacía
lista=[]

# Se agregan elementos a la lista con el método append()
lista.append(100)
lista.append(50)
lista.append(-100)
lista.append(20)
lista.append(5)

# Se imprime la lista completa
print(lista)

# Se inserta un elemento en la penúltima posición de la lista con el método insert()
lista.insert(-2,'prueba')

# Se imprime la lista con el nuevo elemento insertado
print(lista)
